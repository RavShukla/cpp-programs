#include <iostream>
using namespace std;
int main() {
	//reverse star triangle 
 int m ;
 cout << "enter no. of rows";
 cin >> m;
 int a = 1 ;
 
 for(int i=1 ; i<=m ; i++){
 	
 	
 	for(int j=1 ; j <= i ; j++ ){
 		
 		cout<<a<<" ";
 		cout<<a++;
 	
 		
	 }
	 
	 cout<< endl ;
 
}
}
